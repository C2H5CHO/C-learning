// 2、基础数据类型
#include <bits/stdc++.h>
using namespace std;

int main(){
	int i = 3; // 整数int
	double d = 3.14159; // 双精度浮点数double
	char c = 'A'; // 字符char
	char s[] = "Hello"; // 字符串s
	bool b = true; // 布尔值bool
	
	cout << "整数int：" << i << '\n';
	cout << "双精度浮点数double：" << d << '\n';
	cout << "字符char：" << c << '\n';
	cout << "字符串s：" << s << '\n';
	cout << "布尔值bool：" << b << '\n';
	
	return 0;
} 
